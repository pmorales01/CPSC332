CREATE TABLE PROFESSOR (SSN NUMERIC(9) PRIMARY KEY,
    PNAME VARCHAR(50),
    STREET_ADDRESS VARCHAR(30),
    CITY VARCHAR(20),
    STATE CHAR(2),
    ZIP_CODE CHAR(5),
    AREA_CODE NUMERIC(3),
    DIGIT_NUM NUMERIC(7),
    SEX ENUM('F', 'M'),
    TITLE VARCHAR(30),
    SALARY NUMERIC(6));

CREATE TABLE DEPARTMENT (NUMBER NUMERIC(10) PRIMARY KEY,
    DNAME VARCHAR(30),
    TELEPHONE NUMERIC(10),
    OFFICE_LOCATION VARCHAR(30),
    PROFESSOR_SSN NUMERIC(9),
    FOREIGN KEY (PROFESSOR_SSN) REFERENCES PROFESSOR(SSN),
    CHAIRPERSON VARCHAR(50));

CREATE TABLE COURSE (NUMBER VARCHAR(10) PRIMARY KEY, 
    TITLE VARCHAR(30),
    TEXTBOOK VARCHAR(50),
    UNITS TINYINT,
    DNUMBER NUMERIC(10),
    FOREIGN KEY (DNUMBER) REFERENCES DEPARTMENT(NUMBER));

CREATE TABLE SECTION (CNUMBER VARCHAR(10),
    FOREIGN KEY (CNUMBER) REFERENCES COURSE(NUMBER),
    SECTION_NUMBER NUMERIC(10),
    CLASSROOM VARCHAR(30),
    NUMBER_OF_SEATS NUMERIC(3),
    BEGINNING_TIME VARCHAR(10),
    ENDING_TIME VARCHAR(10),
    PRIMARY KEY (CNUMBER, SECTION_NUMBER), 
    PROFESSOR_SSN NUMERIC(9),                  
    FOREIGN KEY (PROFESSOR_SSN) REFERENCES PROFESSOR(SSN));

CREATE TABLE STUDENT (CAMPUS_WIDE_ID NUMERIC(9) PRIMARY KEY,
    FNAME VARCHAR(30),
    LNAME VARCHAR(30),
    ADDRESS VARCHAR(50),
    TELEPHONE NUMERIC(10),
    DNUMBER NUMERIC(10),
    FOREIGN KEY (DNUMBER) REFERENCES DEPARTMENT(NUMBER));

CREATE TABLE ENROLLMENT (CAMPUS_WIDE_ID NUMERIC(9),
    FOREIGN KEY (CAMPUS_WIDE_ID) REFERENCES STUDENT(CAMPUS_WIDE_ID),
    CNUMBER VARCHAR(10),
    SECTION_NUMBER NUMERIC(10),
    FOREIGN KEY (CNUMBER, SECTION_NUMBER) REFERENCES SECTION(CNUMBER, SECTION_NUMBER),
    GRADE ENUM ('A+', 'A', 'A-', 'B+', 'B', 'B-', 'C+', 'C', 'C-', 'D+', 'D', 'D-', 'F', 'W'),
    PRIMARY KEY (CAMPUS_WIDE_ID, CNUMBER, SECTION_NUMBER));

CREATE TABLE MINORS (CAMPUS_WIDE_ID NUMERIC(9),
    FOREIGN KEY (CAMPUS_WIDE_ID) REFERENCES STUDENT(CAMPUS_WIDE_ID),
    DNUMBER NUMERIC(10),
    FOREIGN KEY (DNUMBER) REFERENCES DEPARTMENT(NUMBER),
    PRIMARY KEY (CAMPUS_WIDE_ID, DNUMBER));

CREATE TABLE PROFESSOR_COLLEGE_DEGREES (PROFESSOR_SSN NUMERIC(9),
    FOREIGN KEY (PROFESSOR_SSN) REFERENCES PROFESSOR(SSN), 
    COLLEGE_DEGREE VARCHAR (30), 
    PRIMARY KEY(PROFESSOR_SSN, COLLEGE_DEGREE));

CREATE TABLE PREREQUISITE_COURSE (CNUMBER VARCHAR(10) REFERENCES COURSE(NUMBER),
    PREREQUISITE_NUMBER VARCHAR(10) REFERENCES COURSE(NUMBER),
    PRIMARY KEY (CNUMBER, PREREQUISITE_NUMBER));

CREATE TABLE SECTION_MEETING_DAYS (CNUMBER VARCHAR(10) REFERENCES SECTION(CNUMBER),
    SECTION_NUMBER NUMERIC(10) REFERENCES SECTION(SECTION_NUMBER),
    MEETING_DAY ENUM ('Mon', 'Tues', 'Wed', 'Thurs', 'Fri', 'Sat'),
    PRIMARY KEY (CNUMBER, SECTION_NUMBER, MEETING_DAY));

